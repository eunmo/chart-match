const { refDateWeek } = require('../util');

test.each([
  ['2010-01-02', 2010, '02'],
  ['2010-12-18', 2010, '52'],
  ['2010-12-25', 2011, '01'],
  ['2011-12-24', 2011, '53'],
  ['2011-12-31', 2012, '01'],
  ['2012-12-22', 2012, '52'],
  ['2012-12-29', 2013, '01'],
  ['2013-12-21', 2013, '52'],
  ['2013-12-28', 2014, '01'],
  ['2014-12-20', 2014, '52'],
  ['2014-12-27', 2015, '01'],
  ['2015-12-19', 2015, '52'],
  ['2015-12-26', 2015, '53'],
  ['2016-01-02', 2016, '01'],
  ['2016-12-31', 2016, '53'],
  ['2017-01-07', 2017, '01'],
  ['2017-12-30', 2017, '52'],
  ['2018-01-06', 2018, '01'],
  ['2018-12-29', 2018, '52'],
  ['2019-01-05', 2019, '01'],
  ['2019-12-28', 2019, '52'],
  ['2020-01-04', 2020, '01'],
])('refDateWeek %s', (date, year, week) => {
  const [y, w] = refDateWeek(date, 0, 6);
  expect(y).toBe(year);
  expect(w).toBe(week);
});
